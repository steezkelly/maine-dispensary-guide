$projectRoot = "C:\Users\Steve\OpenCode Projects\project-1"
$guidesPath = "$projectRoot\src\pages\guides"
$dashboardPath = "$projectRoot\MISSION_CONTROL.md"
$now = Get-Date -Format "yyyy-MM-dd HH:mm:ss"

Write-Host "🚀 Starting Project Health Audit..." -ForegroundColor Cyan

# 1. System Status
$dnsCheck = "Checking..."
try {
    $dns = Resolve-DnsName mainedispensaryguide.com -ErrorAction Stop
    $dnsCheck = "✅ Active"
} catch {
    $dnsCheck = "❌ DNS Error"
}

$sslCheck = "Checking..."
try {
    $web = Invoke-WebRequest -Uri "https://mainedispensaryguide.com" -Method Head -UseBasicParsing -ErrorAction Stop
    $sslCheck = "✅ Active"
} catch {
    $sslCheck = "⚠️ SSL Pending/Error"
}

# 2. Content Audit
$guideFiles = Get-ChildItem -Path $guidesPath -Filter "*.astro"
$totalGuides = $guideFiles.Count
$optimizedCount = 0
$totalWords = 0

foreach ($file in $guideFiles) {
    $content = Get-Content -Path $file.FullName -Raw
    $isOptimized = ($content -match "Callout") -and ($content -match "heroImage") -and ($content -match "further-reading")
    if ($isOptimized) { $optimizedCount++ }
    
    # Rough word count (stripping tags)
    $cleanText = $content -replace '<[^>]+>', ''
    $totalWords += ($cleanText.Split(' ', [System.StringSplitOptions]::RemoveEmptyEntries)).Count
}

$avgWords = [math]::Round($totalWords / $totalGuides)

# 3. Build Dashboard
$mdContent = @"
